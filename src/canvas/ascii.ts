export function getCrushAscii(errorText: string): string {
  const snippet = errorText.slice(0, 30).replace(/\n/g, " ");
  return `╔══════════════════════╗
║   🐛 Bug 粉碎报告      ║
╠══════════════════════╣
║ "${snippet}..."  ║
║                       ║
║ 状态: ✅ 已粉碎       ║
║ 建议: 重启试试        ║
╚══════════════════════╝
(这个错误已经被宠物吞掉了)`;
}

